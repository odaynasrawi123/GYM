            <h2>History</h2>
            <p>In December 1891, James Naismith, a Canadian professor of physical education and instructor at the
                International Young Men's Christian Association Training School (now Springfield College) in Springfield,
                Massachusetts, was trying to keep his gym class active on a rainy day. He sought a vigorous indoor game to
                keep his students occupied and at proper levels of fitness during the long New England winters,After
                rejecting other ideas as either too rough or poorly suited to walled-in gymnasiums, he invented a new game
                in which players would pass a ball to teammates and try to score points by tossing the ball into a basket
                mounted on a wall.</p>

