            <h2>Rules and regulations</h2>
            <p>Measurements and time limits discussed in this section often vary among tournaments and organizations; international and NBA rules are used in this section. The object of the game is to outscore one's opponents by throwing the ball through the opponents' basket from above while preventing the opponents from doing so on their own. An attempt to score in this way is called a shot. A successful shot is worth two points, or three points if it is taken from beyond the three-point arc 6.75 metres (22 ft 2 in) from the basket in international games[34] and 23 feet 9 inches (7.24 m) in NBA games. A one-point shot can be earned when shooting from the foul line after a foul is made. After a team has scored from a field goal or free throw, play is resumed with a throw-in awarded to the non-scoring team taken from a point beyond the endline of the court where the points(s) were scored.</p>
            <h2>Height</h2>
            <p>At the professional level, most male players are above 6 feet 3 inches (1.91 m) and most women above 5 feet 7 inches (1.70 m). Guards, for whom physical coordination and ball-handling skills are crucial, tend to be the smallest players. Almost all forwards in the top men's pro leagues are 6 feet 6 inches (1.98 m) or taller. Most centers are over 6 feet 10 inches (2.08 m) tall. According to a survey given to all NBA teams,[when?] the average height of all NBA players is just under 6 feet 7 inches (2.01 m), with the average weight being close to 222 pounds (101 kg). The tallest players ever in the NBA were Manute Bol and Gheorghe Mureșan, who were both 7 feet 7 inches (2.31 m) tall. At 7 feet 2 inches (2.18 m), Margo Dydek was the tallest player in the history of the WNBA.</p>
