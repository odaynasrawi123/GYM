            <h2>Name</h2>
            <p>Football is one of a family of football codes, which emerged from various ball games played worldwide
                since antiquity. The modern game traces its origins to 1863 when the Laws of the Game were originally
                codified in England by The Football Association (FA). The name association football was coined to
                distinguish the game from the other forms of football played at the time, specifically rugby football.
                The first written "reference to the inflated ball used in the game" was in the mid-14th century: "Þe
                heued fro þe body went, Als it were a foteballe." The Online Etymology Dictionary states that the "rules
                of the game" were made in 1848, before the "split off in 1863".</p>
            <h2>Laws</h2>
            <p>There are 17 laws in the official Laws of the Game, each containing a collection of stipulation and
                guidelines. The same laws are designed to apply to all levels of football, although certain
                modifications for groups such as juniors, seniors, women and people with physical disabilities are
                permitted.</p>
