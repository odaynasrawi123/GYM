            <h2>History </h2>
            <p>
                Kicking ball games arose independently multiple times across multiple cultures. According to FIFA, the
                Chinese competitive game cuju (蹴鞠, literally "kick ball") is the earliest form of football for which
                there
                is evidence. Cuju players could use any part of the body apart from hands and the intent was kicking a
                ball
                through an opening into a net. It was remarkably similar to modern football, though similarities to
                rugby
                occurred. During the Han Dynasty (206 BCE – 220 CE), cuju games were standardised and rules were
                established
            </p>

