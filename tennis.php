            <h2>Officials</h2>
            <p>In most professional play and some amateur competition, there is an officiating head judge or chair
                umpire (usually referred to simply as the umpire), who sits in a raised chair to one side of the court.
                The umpire has absolute authority to make factual determinations. The umpire may be assisted by line
                judges, who determine whether the ball has landed within the required part of the court and who also
                call foot faults. There also may be a net judge who determines whether the ball has touched the net
                during service. The umpire has the right to overrule a line judge or a net judge if the umpire is sure
                that a clear mistake has been made.</p>
            <h2>Injuries</h2>
            <p>Muscle strain is one of the most common injuries in tennis.[83] When an isolated large-energy appears
                during the muscle contraction and at the same time body weight apply huge amount of pressure to the
                lengthened muscle, muscle strain can occur.[84] Inflammation and bleeding are triggered when muscle
                strain occurs, which can result in redness, pain and swelling.[84] Overuse is also common in tennis
                players of all levels. Muscle, cartilage, nerves, bursae, ligaments and tendons may be damaged from
                overuse. The repetitive use of a particular muscle without time for repair and recovery is the most
                common cause of injury.</p>
