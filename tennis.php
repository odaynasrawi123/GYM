            <h2>History</h2>
            <p>
                Historians believe that the game's ancient origin lay in 12th century northern France, where a ball was
                struck with the palm of the hand. Louis X of France was a keen player of jeu de paume ("game of the
                palm"),
                which evolved into real tennis, and became notable as the first person to construct indoor tennis courts
                in
                the modern style. Louis was unhappy with playing tennis outdoors and accordingly had indoor, enclosed
                courts
                made in Paris "around the end of the 13th century". In due course this design spread across royal
                palaces
                all over Europe. In June 1316 at Vincennes, Val-de-Marne and following a particularly exhausting game,
                Louis
                drank a large quantity of cooled wine and subsequently died of either pneumonia or pleurisy, although
                there
                was also suspicion of poisoning. Because of the contemporary accounts of his death, Louis X is history's
                first tennis player known by name. Another of the early enthusiasts of the game was King Charles V of
                France, who had a court set up at the Louvre Palace.
