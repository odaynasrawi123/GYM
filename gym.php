            <h2>History</h2>
            <p>
                The first recorded gymnasiums date back to over 3000 years ago in ancient Persia, where they were known
                as
                zurkhaneh, areas that encouraged physical fitness. The larger Roman Baths often had attached fitness
                facilities, the baths themselves sometimes being decorated with mosaics of local champions of sport.
                Gyms in
                Germany were an outgrowth of the Turnplatz, an outdoor space for gymnastics founded by German educator
                Friedrich Jahn in 1811 and later promoted by the Turners, a nineteenth-century political and gymnastic
                movement. The first American to open a public gym in the United States using Jahn's model was John Neal
                of
                Portland, Maine in 1827. The first indoor gymnasium in Germany was probably the one built in Hesse in
                1852 by Adolph Spiess.
