            <h2>Gym</h2>
            <p class="firstP">A gymnasium, also known as a gym, is a covered location for athletics. The word is derived
                from the ancient
                Greek gymnasium. They are commonly found in athletic and fitness centres, and as activity and learning
                spaces in educational institutions. " Gym" is also slang for "fitness centre" , which is often an area
                for indoor recreation. A gym may be open air as well. A gym is a place with a number of equipments and
                machines used by the people to do exercises.</p>
            <p>Gymnasia apparatuses such as barbells, jumping board, running path, tennis-balls, cricket field, and
                fencing
                area are used as exercises. In safe weather, outdoor locations are the most conducive to health. Gyms
                were popular in ancient Greece. Their curricula included self-defense, gymnastica medica, or physical
                therapy to help the sick and injured, and for physical fitness and sports, from boxing to dancing to
                skipping rope.</p>
