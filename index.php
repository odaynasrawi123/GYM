            <h2> Sports and education</h2>
            <p>Research suggests that sports have the capacity to connect youth to positive adult role models and
                provide positive development opportunities, as well as promote the learning and application of life
                skills. In recent years the use of sport to reduce crime, as well as to prevent violent extremism and
                radicalization, has become more widespread, especially as a tool to improve self-esteem, enhance social
                bonds and provide participants with a feeling of purpose.
                There is no high-quality evidence that shows the effectiveness of interventions to increase sports
                participation of the community in sports such as mass media campaigns, educational sessions, and policy
                changes. There is also no high-quality studies that investigate the effect of such interventions in
                promoting healthy behavior change in the community.</p>
