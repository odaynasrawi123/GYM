            <h2> Sport</h2>
            <p class="firstP">Sport pertains to any form of competitive physical activity or game that aims to use,
                maintain or improve physical ability and skills while providing enjoyment to participants and, in some
                cases, entertainment to spectators. Sports can, through casual or organized participation, improve one's
                physical health. Hundreds of sports exist, from those between single contestants, through to those with
                hundreds of simultaneous participants, either in teams or competing as individuals. In certain sports
                such as racing, many contestants may compete, simultaneously or consecutively, with one winner; in
                others, the contest (a match) is between two sides</p>
            <img src=" ./images/download.jpg" alt="400">
            <p>Sport is generally recognised as system of activities based in physical athleticism or physical
                dexterity, with major competitions such as the Olympic Games admitting only sports meeting this
                definition. Other organisations, such as the Council of Europe, preclude activities without a physical
                element from classification as sports. However, a number of competitive, but non-physical, activities
                claim recognition as mind sports. The International Olympic Committee (through ARISF) recognises both
                chess and bridge as bona fide sports, and SportAccord, the international sports federation association,
                recognises five non-physical sports: bridge, chess, draughts (checkers), Go and xiangqi, and limits the
                number of mind games which can be admitted as sports.</p>

            <h2> History</h2>
            <p>The history of sports extends back to the Ancient world. The physical activity that developed into sports
                had early links with ritual, warfare and entertainment</p>
            <p> Study of the history of sport can teach lessons about social changes and about the nature of sport
                itself, as sport seems involved in the development of basic human skills (compare play).[citation
                needed] As one delves further back in history, dwindling evidence makes theories of the origins and
                purposes of sport more and more difficult to support.</p>


